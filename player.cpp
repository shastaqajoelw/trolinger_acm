/** Simple representation for a pusher. */
struct Pusher {
  /* Position of the pusher */
  Vector2D pos;

  /* Pusher velocity */
  Vector2D vel;

};

/** Simple representation for a marker */
struct Marker {
  /* Position of the marker */
  Vector2D pos;

  /* Marker velocity */
  Vector2D vel;

  /* Marker color */
  int color;
};

